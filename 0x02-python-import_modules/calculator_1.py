#!/usr/bin/python3

def add(a, b):
    """add function

        a: first number
        b: second number

    The return . a + b
    """
    return (a + b)


def sub(a, b):
    """sub function

        a: first number
        b: second number

    The return . a - b
    """
    return (a - b)


def mul(a, b):
    """multi function

        a: first number
        b: second number

    The return . a * b
    """
    return (a * b)


def div(a, b):
    """div function

        a: first number
        b: second number

    The return . a / b
    """
    return int(a / b)
